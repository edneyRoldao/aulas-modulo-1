// aula operadores matematicos

// base dos exemplos
let n1 = 10;
let n2 = 20;
let resultado;

// adicao
resultado = n1 + n2;
console.log("exemplo adicao");
console.log(resultado);

// subtracao
resultado = n2 - n1;
console.log("exemplo subtracao");
console.log(resultado);

// multiplicacao
resultado = n2 * n1;
console.log("exemplo multiplicacao");
console.log(resultado);


// divisao
resultado = n2 / n1;
console.log("exemplo divisao");
console.log(resultado);


// exemplo resto da divisao
// quando a divisao nao for exato, retorna 1, exp 25/2 = 12.5
// quando divisao exato, retorna 0, exp 20/2 = 10.0
n1 = 20;
n2 = 2;
resultado = n1 % n2;
console.log("exemplo resto da divisao");
console.log(resultado);

// Operadores de incremento (++) e decremento (--)
n1 = 10;
resultado = ++n1;
console.log("exemplo incremento");
console.log(resultado);
console.log(n1);

// Operadores de incremento (++) e decremento (--)
n1 = 10;
resultado = --n1;
console.log("exemplo decremento");
console.log(resultado);
console.log(n1);
